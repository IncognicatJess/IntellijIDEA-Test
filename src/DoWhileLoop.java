public class DoWhileLoop {
    public static void main(String[] args) {

        var counter= 1;

        do{
            System.out.println("Perulangan ke-" + counter);
            counter++;
        } while(counter <= 10);

        //while membuat kode berulang minimal satu kali "Do While (Lakukan lari Sementara sampai lelah)"
    }

}
